package day20;

public class WhileLoopTwo {
     public static void main(String[] args) {
	int count = 1;
	while (count<=1000) {
		System.out.print(count+" ");
		count++;
	}
	System.out.println();
	System.out.println("Count:" + count);
	
	
	int index = 10;
	while (index >=1) {
		System.out.print(index + " ");
		index--;
	}
	System.out.println();
	System.out.println("index:" + index);
}
}
